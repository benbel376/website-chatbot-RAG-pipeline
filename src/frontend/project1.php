        <h2 class="project-detail-title">Digital AD Inventory Bidding</h2>
        <h3 class="project-detail-subtitle">Subtitle: Building a Bidlist Optimization Algorithm for Real-Time Bidding (RTB) on The Trade Desk (TTD)</h3>

        <p class="project-detail-description">
        The objective of this project was to design a scalable, data-driven bidlist optimization algorithm for real-time bidding (RTB) on The Trade Desk (TTD) platform. This algorithm processes ad inventory data to select optimal bids, maximizing engagement and performance within budget constraints. The project involved creating a robust data pipeline that extracts, cleans, and transforms RTB data to develop a scoring system that prioritizes inventory based on key performance indicators (KPIs) such as engagement rate and click-through rate. The result is a streamlined, effective bidlist generation that enhances targeting and ROI for advertisers.
        </p>
