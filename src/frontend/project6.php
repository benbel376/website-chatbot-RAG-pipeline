    <div class="detail-section">
        <h2 class="project-detail-title">Amharic RAG Ad Builder</h2>
        <h3 class="project-detail-subtitle">Subtitle: Enabling Quality Embedding and Text Generation for Amharic Language</h3>

        <p class="project-detail-description">
            This project, initiated by AIQEM, an African startup specializing in AI and Blockchain solutions, aims to enhance Amharic text manipulation for digital advertising. The Amharic RAG Ad Builder generates engaging ad content for Telegram, utilizing a Retrieval-Augmented Generation (RAG) pipeline fine-tuned for the Amharic language. Leveraging open-source LLMs and advanced language processing techniques, this solution ensures that ads are relevant, culturally aligned, and impactful within the Telegram ecosystem.
        </p>
        <p class="project-detail-date">Date: November 1, 2024</p>
   
