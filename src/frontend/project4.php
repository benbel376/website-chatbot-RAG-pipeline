    <div class="detail-section">
        <h2 class="project-detail-title">Hotel Review Sentiment and Topic Analysis</h2>
        <h3 class="project-detail-subtitle">Subtitle: Sentiment Analysis and Topic Modeling on Hotel Reviews</h3>

        <p class="project-detail-description">
            This project performs sentiment analysis and topic modeling on hotel reviews to understand customer feedback and uncover valuable insights. The process involves pre-processing review texts, classifying them by sentiment, extracting meaningful topics, and clustering similar reviews. The analysis is conducted using Python's NLP and machine learning libraries, with the final goal of identifying trends and patterns in customer sentiments regarding various aspects of hotel services.
        </p>
        <p class="project-detail-date">Date: November 1, 2024</p>
    </div>

